def ensure_dir(p: Path) -> None:
    p.mkdir(parents=True, exist_ok=True)

def parse_case_id(path_str: str) -> str:
    """Extract 'sXXXX' from a path like 's0736/ct.nii.gz' or 's0736\\ct.npy'."""
    parts = re.split(r"[\\/]+", str(path_str).strip())
    return parts[0] if parts else str(path_str)

def safe_read_json(path: Path) -> Optional[dict]:
    try:
        with path.open("r", encoding="utf-8") as f:
            return json.load(f)
    except Exception:
        return None

def load_npy_3d(path: Path) -> np.ndarray:
    """Load npy and return a 3D array (D,H,W). Accepts [1,D,H,W] or [D,H,W]."""
    arr = np.load(str(path), mmap_mode="r")
    arr = np.asarray(arr)
    if arr.ndim == 4 and arr.shape[0] == 1:
        arr = arr[0]
    if arr.ndim != 3:
        raise ValueError(f"Expected 3D array after squeeze, got shape {arr.shape} from {path}")
    return arr

def text_flags(text: str) -> Tuple[int, int, str, str]:
    """
    Return:
      has_uncertainty (0/1),
      has_negation (0/1),
      laterality ('left'/'right'/'bilateral'/'none'),
      anatomy_group (coarse group)
    """
    t = f" {str(text).lower()} "
    has_unc = int(any(term in t for term in UNCERTAINTY_TERMS))
    has_neg = int(any(term in t for term in NEGATION_TERMS))

    lat = "none"
    for k, terms in LATERALITY_TERMS.items():
        if any(term in t for term in terms):
            lat = k
            break

    anatomy = "other"
    for group, kws in ANATOMY_GROUPS.items():
        if any(kw in t for kw in kws):
            anatomy = group
            break

    return has_unc, has_neg, lat, anatomy

def word_count(text: str) -> int:
    toks = re.findall(r"[A-Za-z0-9']+", str(text))
    return len(toks)


