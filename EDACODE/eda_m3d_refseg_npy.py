def plot_hist(values: np.ndarray, title: str, xlabel: str, out_path: Path, bins: int = 50, log1p: bool = False) -> None:
    import matplotlib.pyplot as plt

    v = values[~np.isnan(values)]
    if log1p:
        v = np.log1p(v)

    plt.figure(figsize=(7, 4.5))
    plt.hist(v, bins=bins)
    plt.title(title)
    plt.xlabel(xlabel + (" (log1p)" if log1p else ""))
    plt.ylabel("Count")
    plt.tight_layout()
    plt.savefig(out_path, dpi=200)
    plt.close()

def plot_bar(counter: Counter, title: str, out_path: Path, top_k: int = 20) -> None:
    import matplotlib.pyplot as plt

    items = counter.most_common(top_k)
    if not items:
        return
    labels, counts = zip(*items)

    plt.figure(figsize=(10, 4.5))
    plt.bar(range(len(labels)), counts)
    plt.xticks(range(len(labels)), labels, rotation=45, ha="right")
    plt.title(title)
    plt.ylabel("Count")
    plt.tight_layout()
    plt.savefig(out_path, dpi=200)
    plt.close()

def embed_text_tfidf(texts: List[str], max_features: int = 8000) -> np.ndarray:
    from sklearn.feature_extraction.text import TfidfVectorizer
    from sklearn.decomposition import TruncatedSVD

    vectorizer = TfidfVectorizer(
        lowercase=True,
        stop_words="english",
        max_features=max_features,
        ngram_range=(1, 2),
        min_df=2,
    )
    X = vectorizer.fit_transform(texts)

    # reduce sparse -> dense
    n_components = min(50, X.shape[1] - 1) if X.shape[1] > 1 else 1
    if n_components > 1:
        svd = TruncatedSVD(n_components=n_components, random_state=42)
        Xr = svd.fit_transform(X)
    else:
        Xr = X.toarray()
    return Xr.astype(np.float32)

def embed_text_sbert(texts: List[str], model_name: str) -> np.ndarray:
    from sentence_transformers import SentenceTransformer
    model = SentenceTransformer(model_name)
    emb = model.encode(texts, batch_size=32, show_progress_bar=True, normalize_embeddings=True)
    return np.asarray(emb, dtype=np.float32)

def run_tsne(
    X: np.ndarray,
    meta: pd.DataFrame,
    out_prefix: str,
    out_dir: Path,
    color_cols: List[str],
    perplexity: float = 30.0,
    random_state: int = 42,
) -> None:
    """
    Saves:
      - {out_prefix}_tsne_coords.csv
      - scatter plots colored by each column in color_cols
    """
    from sklearn.manifold import TSNE
    import matplotlib.pyplot as plt

    n = X.shape[0]
    if n < 5:
        print(f"[t-SNE] Too few samples ({n}), skip.")
        return

    perplexity = min(perplexity, max(2.0, (n - 1) / 3.0))
    tsne = TSNE(
        n_components=2,
        perplexity=perplexity,
        init="pca",
        learning_rate="auto",
        random_state=random_state,
    )
    Y = tsne.fit_transform(X)

    coords = meta.copy()
    coords["tsne_x"] = Y[:, 0]
    coords["tsne_y"] = Y[:, 1]
    coords.to_csv(out_dir / f"{out_prefix}_tsne_coords.csv", index=False)

    for col in color_cols:
        if col not in coords.columns:
            continue

        plt.figure(figsize=(7, 6))
        c = coords[col]

        if pd.api.types.is_numeric_dtype(c):
            # If numeric but only a few unique values, treat it as categorical (better legend)
            uniq_vals = sorted(pd.Series(c).dropna().unique().tolist())
            if len(uniq_vals) <= 12:
                for v in uniq_vals:
                    idx = (coords[col] == v).values
                    plt.scatter(coords.loc[idx, "tsne_x"], coords.loc[idx, "tsne_y"], s=10, label=str(v))
                plt.legend(title=col, bbox_to_anchor=(1.04, 1), loc="upper left")
            else:
                sc = plt.scatter(coords["tsne_x"], coords["tsne_y"], c=c, s=10)
                plt.colorbar(sc, label=col)
        else:
                # categorical -> plot per category (uses default matplotlib color cycle)
            cats = c.astype(str).fillna("NA")
            uniq = sorted(cats.unique())

            for u in uniq:
                idx = (cats == u).values
                plt.scatter(coords.loc[idx, "tsne_x"], coords.loc[idx, "tsne_y"], s=10, label=u)

            plt.legend(title=col, bbox_to_anchor=(1.04, 1), loc="upper left")

        plt.title(f"t-SNE colored by {col}")
        plt.tight_layout()
        plt.savefig(out_dir / f"{out_prefix}_tsne_{col}.png", dpi=220)
        plt.close()

def make_overlay_png(ct: np.ndarray, bin_mask: np.ndarray, out_path: Path, title: str = "") -> None:
    import matplotlib.pyplot as plt
    if bin_mask.sum() == 0:
        return

    areas = bin_mask.sum(axis=(1, 2))
    z = int(np.argmax(areas))
    img = ct[z]
    m = bin_mask[z].astype(np.uint8)

    plt.figure(figsize=(6, 6))
    plt.imshow(img, cmap="gray")
    try:
        plt.contour(m, levels=[0.5], linewidths=1)
    except Exception:
        pass
    plt.title(title + f" (z={z}, area={int(areas[z])})")
    plt.axis("off")
    plt.tight_layout()
    plt.savefig(out_path, dpi=220)
    plt.close()


